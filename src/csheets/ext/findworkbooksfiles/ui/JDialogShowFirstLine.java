    public void updateText(Cell[] firstRow){
        textArea.setEditable(false);
        String input="";
        for (int i = 0; i < firstRow.length; i++) {
            input+=String.format("| %s ", firstRow[i].getContent());
        }
        textArea.setText(input);
    }
    
