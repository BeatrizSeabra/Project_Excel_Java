    class ColorItemListCellRenderer implements ListCellRenderer {

        protected DefaultListCellRenderer defaultRenderer = new DefaultListCellRenderer();        

        @Override
        public Component getListCellRendererComponent(JList jlist,
                Object value,
                int cellIndex,
                boolean isSelected,
                boolean cellHasFocus) {
            String theText = null;

            JLabel renderer = (JLabel) defaultRenderer.getListCellRendererComponent(jlist, value, cellIndex,
                    isSelected, cellHasFocus);

            if (value instanceof Server) {
                Server server = (Server) value;
                theText = server.getName() + " - " + server.getPort();
                if (server.isRead_only()) {
                    renderer.setFont(new Font("Courier", Font.ITALIC, 12));
                }
            } else if (value instanceof Client) {
                Client client = (Client) value;
                theText = client.getIp() + " - " + client.getPort();
                if (client.isRead_only()) {
                    renderer.setFont(new Font("Courier", Font.ITALIC, 12));
                }
            } else {
                theForeground = new Color(255, 0, 0);
                theText = "erro";
            }

            renderer.setText(theText);
            renderer.setForeground(theForeground);
            renderer.setHorizontalTextPosition(SwingConstants.CENTER);
            return renderer;
        }
    }
