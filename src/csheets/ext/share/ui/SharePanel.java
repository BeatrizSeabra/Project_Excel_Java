    private void btnShareActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnShareActionPerformed

        if (!txtPort.getText().isEmpty() && !jName.getText().isEmpty() && jPasswordField1.getPassword().length > 0) {
            try {

                int porta = Integer.parseInt(txtPort.getText());
                if (porta <= 0) {
                    JOptionPane.showMessageDialog(this, "Please insert a valid number for the field port");
                } else {
                    Address inicio = cells[0][0].getAddress();
                    Address fim = cells[cells.length - 1][cells[0].length - 1].getAddress();

                    Runnable runnable = new Server(String.valueOf(jPasswordField1.getPassword()), Integer.parseInt(txtPort.getText()), inicio, fim, uiController.getActiveSpreadsheet(), jName.getText());
                    Thread thread = new Thread(runnable);
                    thread.start();
                    txtPort.setText("");
                    jName.setText("");
                    jPasswordField1.setText("");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Please insert a number for the field port");
                System.out.println(e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please fill all fields");
        }
    }//GEN-LAST:event_btnShareActionPerformed

